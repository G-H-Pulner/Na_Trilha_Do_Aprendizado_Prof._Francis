var numero

numero = parseFloat(prompt("Digite um número: "))

if (numero % 3 == 0) {
    alert("O numero " + numero + " é múltiplo de 3")
} else {
    alert("O numero " + numero + " não é múltiplo de 3")
}
if (numero % 7 == 0) {
    alert("O numero " + numero + " é múltiplo de 7")
} else {
    alert("O numero " + numero + " não é múltiplo de 7")
}